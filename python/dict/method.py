#  Dict   Method	Description

# clear()	    Removes all the elements from the dictionary
# copy()	    Returns a copy of the dictionary
# fromkeys()	Returns a dictionary with the specified keys and value
# get() 	    Returns the value of the specified key
# items()	    Returns a list containing a tuple for each key value pair
# keys()	    Returns a list containing the dictionary's keys
# pop()	        Removes the element with the specified key
# popitem()	    Removes the last inserted key-value pair
# setdefault()	Returns the value of the specified key. If the key does not exist: insert the key, with the specified value
# update()	    Updates the dictionary with the specified key-value pairs
# values()	    Returns a list of all the values in the dictionary


#        List operations
# Append(): - This function adds an element to the end of the list.
# Extend(): - This function adds multiple elements to the end of the list.
# Insert(): - This function inserts an element at a specified index in the list.
# Remove(): - This function removes the first occurrence of an element from the list.
# Pop(): - This function removes and returns the last element from the list.
# Slice(): - This function returns a new list that is a slice of the original list.
# Reverse(): - This function reverses the order of the elements in the list.
# Len(): - This function returns the length of the list.
# Min(): - This function returns the smallest element in the list.
# Max(): - This function returns the largest element in the list.


